// --------------------loops in javascript--------------------
let i = 0;
for (i = 0; i < 5; i++) {
  console.log("for loop", i);
}

while (i < 5) {
  console.log("while loop", i);
  i++;
}

do {
  console.log("do while loop", i);
  i++;
} while (i < 5);
// --------------------end of loops in javascript--------------------
